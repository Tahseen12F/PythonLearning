# Explain the difference between = Operator and == Operator in Python
""" "=" Operator is an assignment operator which is used to assign the values to the variables
whereas "==" operator is a comparison operator used to compare the values of variables."""
a = 2  # Assignment Operator
print(a)
b = 2
print(a == b)  # Comparison Operator. It returns boolean value

